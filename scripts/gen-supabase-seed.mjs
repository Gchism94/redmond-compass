// Generate supabase/seed.sql from the mock seed (src/data/mock/seed.ts) so Supabase
// is seeded with the same fictional businesses/bulletins/events + News/Resources.
// Run: node scripts/gen-supabase-seed.mjs
import { build } from "esbuild";
import { writeFileSync, mkdtempSync } from "node:fs";
import { tmpdir } from "node:os";
import path from "node:path";

const root = path.resolve(import.meta.dirname, "..");
const tmp = path.join(mkdtempSync(path.join(tmpdir(), "rc-seed-")), "seed.mjs");
await build({
  entryPoints: [path.join(root, "src/data/mock/seed.ts")],
  bundle: true, // type-only imports are erased; no runtime deps
  format: "esm",
  platform: "node",
  outfile: tmp,
  logLevel: "error",
});
const { businesses, bulletins, events, news, resources } = await import(tmp);

// ---- SQL emit helpers ----
const q = (s) => (s == null ? "null" : `'${String(s).replace(/'/g, "''")}'`);
const ts = (s) => (s == null ? "null" : `${q(s)}::timestamptz`);
// event wall-clock times are naive Redmond/Pacific → store the correct instant
const tsPac = (s) => (s == null ? "null" : `'${s.replace("T", " ").replace(/'/g, "''")} America/Los_Angeles'::timestamptz`);
const num = (n) => (n == null ? "null" : Number(n));
const bool = (b) => (b ? "true" : "false");
const arr = (a) => (!a || a.length === 0 ? `'{}'` : `ARRAY[${a.map(q).join(",")}]::text[]`);
const json = (o) => (o == null ? "null" : `${q(JSON.stringify(o))}::jsonb`);

let sql = `-- AUTO-GENERATED by scripts/gen-supabase-seed.mjs from src/data/mock/seed.ts.
-- One-time import so the app runs on real Supabase infra. Re-run to refresh.
-- News + Resources are authored here (Supabase-native). Businesses come from the
-- mock import now; later they sync from GoHighLevel (businesses.ghl_id).
-- Runs as the postgres role on \`supabase db reset\` (bypasses RLS; triggers still fire).

truncate table public.recommendations, public.bulletins, public.events,
  public.news_articles, public.resources, public.businesses restart identity cascade;

`;

sql += "-- businesses\n";
for (const b of businesses) {
  sql += `insert into public.businesses
  (id, name, slug, category, subcategories, description, address, lat, lng, phone, website, email, hours, photos, amenity_tags, claimed, verified, tier, created_at, recommend_count)
values (${q(b.id)}, ${q(b.name)}, ${q(b.slug)}, ${q(b.category)}, ${arr(b.subcategories)}, ${q(b.description)}, ${q(b.address)}, ${num(b.geo?.lat)}, ${num(b.geo?.lng)}, ${q(b.phone)}, ${q(b.website)}, ${q(b.email)}, ${json(b.hours)}, ${arr(b.photos)}, ${arr(b.amenityTags)}, ${bool(b.claimed)}, ${bool(b.verified)}, ${q(b.tier)}, ${ts(b.createdAt)}, ${num(b.recommendCount ?? 0)});\n`;
}

sql += "\n-- bulletins\n";
for (const x of bulletins) {
  sql += `insert into public.bulletins (id, business_id, body, image, link_cta, scheduled_for, status, created_at)
values (${q(x.id)}, ${q(x.businessId)}, ${q(x.body)}, ${q(x.image)}, ${json(x.linkCta)}, ${ts(x.scheduledFor)}, ${q(x.status)}, ${ts(x.createdAt)});\n`;
}

sql += "\n-- events (start/end are Redmond wall-clock → America/Los_Angeles)\n";
for (const e of events) {
  sql += `insert into public.events (id, business_id, title, description, start_at, end_at, venue_name, address, lat, lng, image, category, tags, status)
values (${q(e.id)}, ${q(e.businessId)}, ${q(e.title)}, ${q(e.description)}, ${tsPac(e.startAt)}, ${tsPac(e.endAt)}, ${q(e.venueName)}, ${q(e.address)}, ${num(e.geo?.lat)}, ${num(e.geo?.lng)}, ${q(e.image)}, ${q(e.category)}, ${arr(e.tags)}, ${q(e.status)});\n`;
}

sql += "\n-- news_articles\n";
for (const n of news) {
  sql += `insert into public.news_articles (id, title, slug, excerpt, body, image, source, author, published_at)
values (${q(n.id)}, ${q(n.title)}, ${q(n.slug)}, ${q(n.excerpt)}, ${q(n.body)}, ${q(n.image)}, ${q(n.source)}, ${q(n.author)}, ${ts(n.publishedAt)});\n`;
}

sql += "\n-- resources\n";
for (const r of resources) {
  sql += `insert into public.resources (id, name, category, description, phone, url, address)
values (${q(r.id)}, ${q(r.name)}, ${q(r.category)}, ${q(r.description)}, ${q(r.phone)}, ${q(r.url)}, ${q(r.address)});\n`;
}

writeFileSync(path.join(root, "supabase/seed.sql"), sql);
console.log(`wrote supabase/seed.sql — ${businesses.length} businesses, ${bulletins.length} bulletins, ${events.length} events, ${news.length} news, ${resources.length} resources`);
